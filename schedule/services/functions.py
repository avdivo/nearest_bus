import re

def format_bus_number(bus_num: str) -> str:
    """
    Форматирует номер автобуса, заключая буквенную часть в двойные кавычки.
    Цифровая часть всегда в начале, буквы — только после цифр.

    Примеры:
        "5"    → "5"
        "3а"   → "3\"а\""
        "12б"  → "12\"б\""

    :param bus_num: Строка с номером автобуса (например, "3а", "12б", "5")
    :return: Отформатированная строка с буквенной частью в двойных кавычках
    """
    return re.sub(r'([a-zA-Zа-яА-Я]+)', r'"\1"', bus_num)
    